public class JaxWSManagerException extends Exception {

    public JaxWSManagerException() {
        super();
    }

    public JaxWSManagerException(String message) {
        super(message);
    }

    public JaxWSManagerException(String message, Throwable cause) {
        super(message, cause);
    }

    public JaxWSManagerException(Throwable cause) {
        super(cause);
    }

}
